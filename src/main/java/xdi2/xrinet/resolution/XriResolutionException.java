package xdi2.xri2xdi.resolution;

public class XriResolutionException extends Exception {

	private static final long serialVersionUID = 4500240992794516180L;

	public XriResolutionException() {

		super();
	}

	public XriResolutionException(String message) {

		super(message);
	}

	public XriResolutionException(Throwable cause) {

		super(cause);
	}

	public XriResolutionException(String message, Throwable cause) {

		super(message, cause);
	}
}
